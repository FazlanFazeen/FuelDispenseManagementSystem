public class DateTime {
    private int year;
    private int month;
    private int date;
    private int hour;
    private int minute;
    private String dieselOrPetrol;
    private int fuelDistributed;




    //Getters and Setters
    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public int getMinute() {
        return hour;
    }

    public void setMinute(int hour) {
        this.hour = hour;
    }
    public String getDieselOrPetrol() {
        return dieselOrPetrol;
    }

    public void setDieselOrPetrol(String dieselOrPetrol) {
        this.dieselOrPetrol = dieselOrPetrol;
    }

    public int getFuelDistributed() {
        return fuelDistributed;
    }

    public void setFuelDistributed(int fuelDistributed) {
        this.fuelDistributed = fuelDistributed;
    }
}
